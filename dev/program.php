        <p>The Software Systems Developer (SSD) program (Web Programmer option) is an eight month, full-time business application development program offered by BCIT's School of Computing and Academic Studies, within the HTP department at the downtown Vancouver campus. 
        It prepares individuals for high-demand careers in the technical areas of web application development, programming, database and systems analysis. Students learn industry-ready skills through extensive hands-on practice in current scripting and coding techniques, object-oriented architecture patterns, application security and scalability, developer tools, and platforms for building complete web-based business applications from scratch. 
        Graduates of this certificate program may apply for acceptance into the BCIT Bachelor of Technology in Computer Systems (additional prerequisites are required). 
        This program is delivered through classroom instruction, hands-on lab work and individual and team projects. While applicants are not required to have formal computer programming experience, they need to have a solid understanding of object oriented programming as well as possess sound logical and reasoning skills. Applicants with no knowledge are advised to take a programming language course, such as introduction to object-oriented programming, C#, or Java.</p>
